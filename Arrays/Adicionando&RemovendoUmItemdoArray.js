let users = []

// Adiciona um item no final do Array   
users.push("Gabriel", "Joao", "Marcos")
console.log(users)

// Adiciona um item no comeco do Array
users.unshift("Ana", "Pedro")
console.log(users) 

// Remove o item do inico do Array
users.shift()
console.log(users)

// Remove um item do final do Array
users.pop()
console.log(users)